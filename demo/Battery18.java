package com.smartbattery.demo;

public class Battery18 {

	public static void main(String[] args) {
		String brewStrength = "Strong"; // Example selection 
		if (brewStrength.equals("Strong")) { 
		System.out.println("Preparing strong coffee."); 
		} else if (brewStrength.equals("Regular")) { 
		System.out.println("Preparing regular coffee."); 
		} else { 
		System.out.println("Preparing decaf coffee.");
		}
	}

}
